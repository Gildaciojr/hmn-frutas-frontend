import { create } from "zustand";

interface AuthState {
  ////////////////////////////////////////////////////////////
  // ESTADO
  ////////////////////////////////////////////////////////////

  token: string | null;

  hydrated: boolean;

  ////////////////////////////////////////////////////////////
  // ACTIONS
  ////////////////////////////////////////////////////////////

  setToken: (
    token: string,
  ) => void;

  logout: () => void;

  hydrate: () => void;
}

export const useAuthStore =
  create<AuthState>((set) => ({
    //////////////////////////////////////////////////////////
    // ESTADO INICIAL
    //////////////////////////////////////////////////////////

    token: null,

    hydrated: false,

    //////////////////////////////////////////////////////////
    // SET TOKEN
    //////////////////////////////////////////////////////////

    setToken: (token) => {
      localStorage.setItem(
        "auth_token",
        token,
      );

      set({
        token,
      });
    },

    //////////////////////////////////////////////////////////
    // LOGOUT
    //////////////////////////////////////////////////////////

    logout: () => {
      localStorage.removeItem(
        "auth_token",
      );

      ////////////////////////////////////////////////////////
      // REMOVE COOKIE USADO PELO MIDDLEWARE
      ////////////////////////////////////////////////////////

      document.cookie =
        "token=; expires=Thu, 01 Jan 1970 00:00:00 GMT; path=/";

      set({
        token: null,
      });
    },

    //////////////////////////////////////////////////////////
    // HYDRATE
    //////////////////////////////////////////////////////////

    hydrate: () => {
      const token =
        localStorage.getItem(
          "auth_token",
        );

      set({
        token,
        hydrated: true,
      });
    },
  }));