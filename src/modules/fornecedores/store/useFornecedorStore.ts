import { create } from "zustand";

import type {
  FazendaFornecedor,
  Fornecedor,
  FornecedorHistoricoResponse,
  FornecedorResumoResponse,
} from "../services/fornecedores.service";

interface FornecedorState {
  ////////////////////////////////////////////////////////////
  // FORNECEDOR SELECIONADO
  ////////////////////////////////////////////////////////////

  fornecedorSelecionado: Fornecedor | null;

  ////////////////////////////////////////////////////////////
  // FAZENDA SELECIONADA
  ////////////////////////////////////////////////////////////

  fazendaSelecionada: FazendaFornecedor | null;

  ////////////////////////////////////////////////////////////
  // RESUMO
  ////////////////////////////////////////////////////////////

  fornecedorResumo: FornecedorResumoResponse | null;

  ////////////////////////////////////////////////////////////
  // HISTÓRICO
  ////////////////////////////////////////////////////////////

  fornecedorHistorico: FornecedorHistoricoResponse | null;

  ////////////////////////////////////////////////////////////
  // MODAIS
  ////////////////////////////////////////////////////////////

  resumoOpen: boolean;

  historicoOpen: boolean;

  financeiroOpen: boolean;

  ////////////////////////////////////////////////////////////
  // FORNECEDOR
  ////////////////////////////////////////////////////////////

  selecionarFornecedor: (fornecedor: Fornecedor | null) => void;

  limparFornecedor: () => void;

  ////////////////////////////////////////////////////////////
  // FAZENDA
  ////////////////////////////////////////////////////////////

  selecionarFazenda: (fazenda: FazendaFornecedor | null) => void;

  limparFazenda: () => void;

  ////////////////////////////////////////////////////////////
  // RESUMO
  ////////////////////////////////////////////////////////////

  setFornecedorResumo: (resumo: FornecedorResumoResponse | null) => void;

  ////////////////////////////////////////////////////////////
  // HISTÓRICO
  ////////////////////////////////////////////////////////////

  setFornecedorHistorico: (
    historico: FornecedorHistoricoResponse | null,
  ) => void;

  ////////////////////////////////////////////////////////////
  // MODAIS
  ////////////////////////////////////////////////////////////

  openResumo: () => void;

  closeResumo: () => void;

  openHistorico: () => void;

  closeHistorico: () => void;

  openFinanceiro: () => void;

  closeFinanceiro: () => void;

  ////////////////////////////////////////////////////////////
  // RESET
  ////////////////////////////////////////////////////////////

  reset: () => void;
}

export const useFornecedorStore = create<FornecedorState>((set) => ({
  //////////////////////////////////////////////////////////
  // ESTADOS
  //////////////////////////////////////////////////////////

  fornecedorSelecionado: null,

  fazendaSelecionada: null,

  fornecedorResumo: null,

  fornecedorHistorico: null,

  //////////////////////////////////////////////////////////
  // MODAIS
  //////////////////////////////////////////////////////////

  resumoOpen: false,

  historicoOpen: false,

  financeiroOpen: false,

  //////////////////////////////////////////////////////////
  // FORNECEDOR
  //////////////////////////////////////////////////////////

  selecionarFornecedor: (fornecedor) =>
    set({
      fornecedorSelecionado: fornecedor,
    }),

  limparFornecedor: () =>
    set({
      fornecedorSelecionado: null,
    }),

  //////////////////////////////////////////////////////////
  // FAZENDA
  //////////////////////////////////////////////////////////

  selecionarFazenda: (fazenda) =>
    set({
      fazendaSelecionada: fazenda,
    }),

  limparFazenda: () =>
    set({
      fazendaSelecionada: null,
    }),

  //////////////////////////////////////////////////////////
  // RESUMO
  //////////////////////////////////////////////////////////

  setFornecedorResumo: (resumo) =>
    set({
      fornecedorResumo: resumo,
    }),

  //////////////////////////////////////////////////////////
  // HISTÓRICO
  //////////////////////////////////////////////////////////

  setFornecedorHistorico: (historico) =>
    set({
      fornecedorHistorico: historico,
    }),

  //////////////////////////////////////////////////////////
  // MODAL RESUMO
  //////////////////////////////////////////////////////////

  openResumo: () =>
    set({
      resumoOpen: true,
    }),

  closeResumo: () =>
    set({
      resumoOpen: false,
    }),

  //////////////////////////////////////////////////////////
  // MODAL HISTÓRICO
  //////////////////////////////////////////////////////////

  openHistorico: () =>
    set({
      historicoOpen: true,
    }),

  closeHistorico: () =>
    set({
      historicoOpen: false,
    }),

  //////////////////////////////////////////////////////////
  // MODAL FINANCEIRO
  //////////////////////////////////////////////////////////

  openFinanceiro: () =>
    set({
      financeiroOpen: true,
    }),

  closeFinanceiro: () =>
    set({
      financeiroOpen: false,
    }),

  //////////////////////////////////////////////////////////
  // RESET
  //////////////////////////////////////////////////////////

  reset: () =>
    set({
      fornecedorSelecionado: null,

      fazendaSelecionada: null,

      fornecedorResumo: null,

      fornecedorHistorico: null,

      resumoOpen: false,

      historicoOpen: false,

      financeiroOpen: false,
    }),
}));
