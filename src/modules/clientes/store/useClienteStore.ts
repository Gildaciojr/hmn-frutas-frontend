import { create } from "zustand";

import type {
  Cliente,
  ClienteResumo,
  ClienteHistoricoResponse,
} from "../services/clientes.service";

interface ClienteState {
  ////////////////////////////////////////////////////////////
  // CLIENTE SELECIONADO
  ////////////////////////////////////////////////////////////

  clienteSelecionado: Cliente | null;

  ////////////////////////////////////////////////////////////
  // RESUMO
  ////////////////////////////////////////////////////////////

  clienteResumo: ClienteResumo | null;

  ////////////////////////////////////////////////////////////
  // HISTÓRICO
  ////////////////////////////////////////////////////////////

  clienteHistorico: ClienteHistoricoResponse | null;

  ////////////////////////////////////////////////////////////
  // MODAIS
  ////////////////////////////////////////////////////////////

  resumoOpen: boolean;

  historicoOpen: boolean;

  financeiroOpen: boolean;

  createModalOpen: boolean;

  ////////////////////////////////////////////////////////////
  // ACTIONS CLIENTE
  ////////////////////////////////////////////////////////////

  selecionarCliente: (cliente: Cliente | null) => void;

  limparSelecionado: () => void;

  ////////////////////////////////////////////////////////////
  // ACTIONS RESUMO
  ////////////////////////////////////////////////////////////

  setClienteResumo: (resumo: ClienteResumo | null) => void;

  ////////////////////////////////////////////////////////////
  // ACTIONS HISTÓRICO
  ////////////////////////////////////////////////////////////

  setClienteHistorico: (historico: ClienteHistoricoResponse | null) => void;

  ////////////////////////////////////////////////////////////
  // MODAIS
  ////////////////////////////////////////////////////////////

  openResumo: () => void;

  closeResumo: () => void;

  openHistorico: () => void;

  closeHistorico: () => void;

  openFinanceiro: () => void;

  closeFinanceiro: () => void;

  openCreateModal: () => void;

  closeCreateModal: () => void;

  ////////////////////////////////////////////////////////////
  // RESET
  ////////////////////////////////////////////////////////////

  reset: () => void;
}

export const useClienteStore = create<ClienteState>((set) => ({
  //////////////////////////////////////////////////////////
  // ESTADOS
  //////////////////////////////////////////////////////////

  clienteSelecionado: null,

  clienteResumo: null,

  clienteHistorico: null,

  //////////////////////////////////////////////////////////
  // MODAIS
  //////////////////////////////////////////////////////////

  resumoOpen: false,

  historicoOpen: false,

  financeiroOpen: false,

  createModalOpen: false,

  //////////////////////////////////////////////////////////
  // CLIENTE
  //////////////////////////////////////////////////////////

  selecionarCliente: (cliente) =>
    set({
      clienteSelecionado: cliente,
    }),

  limparSelecionado: () =>
    set({
      clienteSelecionado: null,
    }),

  //////////////////////////////////////////////////////////
  // RESUMO
  //////////////////////////////////////////////////////////

  setClienteResumo: (resumo) =>
    set({
      clienteResumo: resumo,
    }),

  //////////////////////////////////////////////////////////
  // HISTÓRICO
  //////////////////////////////////////////////////////////

  setClienteHistorico: (historico) =>
    set({
      clienteHistorico: historico,
    }),

  //////////////////////////////////////////////////////////
  // MODAL RESUMO
  //////////////////////////////////////////////////////////

  openResumo: () =>
    set({
      resumoOpen: true,
    }),

  closeResumo: () =>
    set({
      resumoOpen: false,
    }),

  //////////////////////////////////////////////////////////
  // MODAL HISTÓRICO
  //////////////////////////////////////////////////////////

  openHistorico: () =>
    set({
      historicoOpen: true,
    }),

  closeHistorico: () =>
    set({
      historicoOpen: false,
    }),

  //////////////////////////////////////////////////////////
  // MODAL FINANCEIRO
  //////////////////////////////////////////////////////////

  openFinanceiro: () =>
    set({
      financeiroOpen: true,
    }),

  closeFinanceiro: () =>
    set({
      financeiroOpen: false,
    }),

  openCreateModal: () =>
    set({
      createModalOpen: true,
    }),

  closeCreateModal: () =>
    set({
      createModalOpen: false,
    }),

  //////////////////////////////////////////////////////////
  // RESET
  //////////////////////////////////////////////////////////

  reset: () =>
    set({
      clienteSelecionado: null,

      clienteResumo: null,

      clienteHistorico: null,

      resumoOpen: false,

      historicoOpen: false,

      financeiroOpen: false,

      createModalOpen: false,
    }),
}));
