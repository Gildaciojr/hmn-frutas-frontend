import { api } from "@/core/http/api";

////////////////////////////////////////////////////////////
// ENUMS
////////////////////////////////////////////////////////////

export type ModeloCaminhao = "TRUCK" | "BITRUCK" | "CARRETA";

export type TipoDescontoCompra =
  | "AUTOMATICO_MODELO"
  | "PERCENTUAL"
  | "MANUAL_KG";

export type StatusCompra = "ABERTA" | "FECHADA" | "CANCELADA";

////////////////////////////////////////////////////////////
// CLIENTE
////////////////////////////////////////////////////////////

export interface CompraCliente {
  id: string;

  nome: string;

  telefone?: string | null;

  documento?: string | null;

  endereco?: string | null;
}

////////////////////////////////////////////////////////////
// FORNECEDOR
////////////////////////////////////////////////////////////

export interface CompraFornecedor {
  id: string;

  nome: string;

  telefone?: string | null;
}

////////////////////////////////////////////////////////////
// FAZENDA FORNECEDOR
////////////////////////////////////////////////////////////

export interface CompraFazendaFornecedor {
  id: string;

  nome: string;
}

////////////////////////////////////////////////////////////
// COMPRA
////////////////////////////////////////////////////////////

export interface Compra {
  id: string;

  //////////////////////////////////////////////////////////
  // CLIENTE
  //////////////////////////////////////////////////////////

  clienteId: string;

  cliente?: CompraCliente;

  //////////////////////////////////////////////////////////
  // FORNECEDOR
  //////////////////////////////////////////////////////////

  fornecedorId?: string | null;

  fornecedor?: CompraFornecedor | null;

  fazendaFornecedorId?: string | null;

  fazendaFornecedor?: CompraFazendaFornecedor | null;

  //////////////////////////////////////////////////////////
  // AUDITORIA
  //////////////////////////////////////////////////////////

  usuarioResponsavelId?: string | null;

  usuarioResponsavelNome?: string | null;

  //////////////////////////////////////////////////////////
  // SNAPSHOTS
  //////////////////////////////////////////////////////////

  clienteNomeSnapshot: string;

  clienteTelefoneSnapshot?: string | null;

  clienteDocumentoSnapshot?: string | null;

  clienteEnderecoSnapshot?: string | null;

  //////////////////////////////////////////////////////////
  // IDENTIFICAÇÃO
  //////////////////////////////////////////////////////////

  safra?: string | null;

  dataCompra: string;

  numeroFolha?: string | null;

  //////////////////////////////////////////////////////////
  // CAMINHÃO
  //////////////////////////////////////////////////////////

  modeloCaminhao: ModeloCaminhao;

  placa: string;

  //////////////////////////////////////////////////////////
  // PESAGEM
  //////////////////////////////////////////////////////////

  kgBruto: number;

  quantidadeFrutas: number;

  mediaFruta: number;

  kgDescontado: number;

  kgLiquido: number;

  //////////////////////////////////////////////////////////
  // DESCONTO
  //////////////////////////////////////////////////////////

  tipoDesconto: TipoDescontoCompra;

  descontoPercentualAplicado?: number | null;

  descontoKgManual?: number | null;

  descontoKgCalculado: number;

  //////////////////////////////////////////////////////////
  // FINANCEIRO
  //////////////////////////////////////////////////////////

  precoKg: string | number;

  totalBruto: string | number;

  despesas: string | number;

  valorTotal: string | number;

  //////////////////////////////////////////////////////////
  // CONTROLE
  //////////////////////////////////////////////////////////

  caminhoes: number;

  status: StatusCompra;

  canceladoEm?: string | null;

  motivoCancelamento?: string | null;

  //////////////////////////////////////////////////////////
  // OBSERVAÇÕES
  //////////////////////////////////////////////////////////

  observacoes?: string | null;

  //////////////////////////////////////////////////////////
  // DATAS
  //////////////////////////////////////////////////////////

  createdAt: string;

  updatedAt: string;
}

////////////////////////////////////////////////////////////
// CREATE PAYLOAD
////////////////////////////////////////////////////////////

export interface CreateCompraPayload {
  //////////////////////////////////////////////////////////
  // CLIENTE
  //////////////////////////////////////////////////////////

  clienteId: string;

  //////////////////////////////////////////////////////////
  // IDENTIFICAÇÃO
  //////////////////////////////////////////////////////////

  safra?: string;

  dataCompra: string;

  numeroFolha?: string;

  //////////////////////////////////////////////////////////
  // CAMINHÃO
  //////////////////////////////////////////////////////////

  modeloCaminhao: ModeloCaminhao;

  placa: string;

  //////////////////////////////////////////////////////////
  // PESAGEM
  //////////////////////////////////////////////////////////

  kgBruto: number;

  quantidadeFrutas: number;

  //////////////////////////////////////////////////////////
  // DESCONTO
  //////////////////////////////////////////////////////////

  tipoDesconto: TipoDescontoCompra;

  descontoPercentualAplicado?: number;

  descontoKgManual?: number;

  //////////////////////////////////////////////////////////
  // FINANCEIRO
  //////////////////////////////////////////////////////////

  precoKg: number;

  despesas?: number;

  valorTotal?: number;

  //////////////////////////////////////////////////////////
  // LEGADO / COMPATIBILIDADE
  //////////////////////////////////////////////////////////

  caminhoes?: number;

  //////////////////////////////////////////////////////////
  // OBSERVAÇÕES
  //////////////////////////////////////////////////////////

  observacoes?: string;
}

////////////////////////////////////////////////////////////
// API RESPONSE
////////////////////////////////////////////////////////////

interface ApiResponse<T> {
  success: boolean;

  data: T;
}

////////////////////////////////////////////////////////////
// GET COMPRAS
////////////////////////////////////////////////////////////

export async function getCompras(): Promise<Compra[]> {
  const response = await api.get<ApiResponse<Compra[]>>("/compras");

  return response.data.data;
}

////////////////////////////////////////////////////////////
// CREATE COMPRA
////////////////////////////////////////////////////////////

export async function createCompra(
  payload: CreateCompraPayload,
): Promise<Compra> {
  const response = await api.post<ApiResponse<Compra>>("/compras", payload);

  return response.data.data;
}
