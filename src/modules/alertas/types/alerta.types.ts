////////////////////////////////////////////////////////////
// ENUMS
////////////////////////////////////////////////////////////

export type CategoriaAlerta =
  | "LIMITE_FINANCEIRO"
  | "VENCIMENTO"
  | "INADIMPLENCIA"
  | "FINANCEIRO"
  | "OPERACIONAL";

export type SeveridadeAlerta =
  | "BAIXA"
  | "MEDIA"
  | "ALTA"
  | "CRITICA";

////////////////////////////////////////////////////////////
// RELAÇÕES
////////////////////////////////////////////////////////////

export interface AlertaCliente {
  readonly id: string;

  readonly nome: string;

  readonly telefone?: string | null;
}

export interface AlertaFornecedor {
  readonly id: string;

  readonly nome: string;

  readonly sobrenome?: string | null;

  readonly telefone?: string | null;
}

////////////////////////////////////////////////////////////
// ALERTA SISTEMA
////////////////////////////////////////////////////////////

export interface AlertaSistema {
  ////////////////////////////////////////////////////////////
  // IDENTIFICAÇÃO
  ////////////////////////////////////////////////////////////

  readonly id: string;

  ////////////////////////////////////////////////////////////
  // CLASSIFICAÇÃO
  ////////////////////////////////////////////////////////////

  readonly categoria: CategoriaAlerta;

  readonly severidade: SeveridadeAlerta;

  ////////////////////////////////////////////////////////////
  // CONTEÚDO
  ////////////////////////////////////////////////////////////

  readonly titulo: string;

  readonly mensagem: string;

  ////////////////////////////////////////////////////////////
  // STATUS
  ////////////////////////////////////////////////////////////

  readonly lido: boolean;

  readonly resolvido: boolean;

  ////////////////////////////////////////////////////////////
  // RELAÇÕES
  ////////////////////////////////////////////////////////////

  readonly clienteId?: string | null;

  readonly fornecedorId?: string | null;

  readonly cliente?: AlertaCliente | null;

  readonly fornecedor?: AlertaFornecedor | null;

  ////////////////////////////////////////////////////////////
  // METADATA
  ////////////////////////////////////////////////////////////

  readonly createdAt: string;

  readonly updatedAt: string;
}

////////////////////////////////////////////////////////////
// HELPERS
////////////////////////////////////////////////////////////

export interface AlertasCounters {
  readonly total: number;

  readonly naoLidos: number;

  readonly ativos: number;

  readonly criticos: number;
}